//城市点击生成地区事件
function cityEvent(cyid){
	$(".cityList input[type=checkbox]").live("click",function(){
		createCounty();
	});
	if(cyid){
		for(var j = 0; j < cyid.length; j++){
			if(j>0){
				$("#county").hide();
				$("#"+cyid[j]).attr("checked","checked");
			}else{
				$("#"+cyid[j]).attr("checked","checked").trigger("click").attr("checked","checked");
			}
		}
	}
	//全选
	$("#city_all").live("click",function(){
		if($(this).attr("checked") == "checked"){
			$(".cityList input[type='checkbox']").attr("checked",true);
			createCounty();
		}else{
			$(".cityList input[type='checkbox']").attr("checked",false);
			$("#county").hide();
		}
	});
};


//生成地区
function createCounty(){
	if($(".cityList").find("input[type=checkbox]:checked").size() == 1){
		$("#county").show();
		var provinceID = $(".cityList").find("input[type=checkbox]:checked").attr("arrayid");
		if(dsy.Exists(provinceID)){
			ar = dsy.Items[provinceID];
			$(".countyList").empty();
			for(i=0;i<ar.length;i++){
				html = '<label><input class="vTT" id="'+ar[i].split("-")[1]+'" arrayid="' + provinceID + '_' + i + '" type="checkbox" />&nbsp;' + ar[i].split("-")[2] + '</label>';
				$(".countyList").append(html);
			}
		};
	}else{
		$("#county").hide();
	};
}


//根据选择的数据动态追加城市文本
function getSelectText(){
	var provinceId = $("#s_province").val();
	var provinceName = $("#s_province").find("option:selected").text();
	var cityId = $("#s_city").val();
	var cityName = $("#s_city").find("option:selected").text();
	var countyId = $("#s_county").val();
	var countyName = $("#s_county").find("option:selected").text();
	
	if(provinceId!="0"){
		$("#s_province_name").val(provinceName);
	}
	if(cityId!="0"){
		$("#s_city_name").val(cityName);
	}
	if(countyId!="0"){
		$("#s_area_name").val(countyName);
	}
}


//地区县
function countyEvent(coutyid){
	if(coutyid){
		for(var s = 0; s < coutyid.length; s++){
			$("#"+coutyid[s]).attr("checked","checked");
		}
	};
	//全选
	$("#county_all").live("click",function(){
		if($(this).attr("checked") == "checked"){
			$(".countyList input[type='checkbox']").attr("checked",true);
		}else{
			$(".countyList input[type='checkbox']").attr("checked",false);
		}
	});
}

//按字母顺序联动初始化函数
function init_area(sid,cyid,coutyid){
	provinceEvent(sid);
	cityEvent(cyid);
	countyEvent(coutyid);
}


